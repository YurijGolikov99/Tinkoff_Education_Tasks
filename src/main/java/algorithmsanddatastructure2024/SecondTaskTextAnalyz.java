package algorithmsanddatastructure2024;

import java.util.Scanner;

/*
Павел обучает свою модель машинного обучения для генерации текста. Обучение искусственного интеллекта — очень непростая задача, однако наш герой уже добился каких-то результатов и теперь хочет оценить качество текстов, которые у него получаются.
Паша пока не придумал хорошего способа оценить получающиеся тексты, поэтому для каждого из них он хочет узнать две простые характеристики — длину минимальной и максимальной строки в полученном тексте.
Помогите начинающему исследователю и напишите программу, которая будет находить необходимые характеристики для сгенерированного текста.

Формат входных данных
В первой строке входного файла дано одно целое число п — длина сгенерированного текста (1 ≤ n ≤10^6).
Во второй строке записан сгенерированный текст, состоящий из строчных и заглавных букв латинского алфавита, нижнего подчеркивания и символа «#». Символ «#» используется в качестве символа переноса строки.

Формат выходных данных
В одной строке выведите два целых числа — минимальную и максимальную длину строки в тексте.

Система оценки
Баллы за каждую подзадачу начисляются только в случае, если все тесты этой подзадачи и необходимых подзадач успешно пройдены.

Примеры данных
Пример 1
Ввод
5
a#aba
Вывод
1 3

Пример 2
Ввод
31
You know#how to solve#this task
Вывод
8 12

Пример 3
Ввод
9
a#b##c#d#
Вывод
0 1

 */
public class SecondTaskTextAnalyz {
    public static void main(String[] args) {
        Scanner scanner = new Scanner(System.in);

        // Считываем длину сгенерированного текста
        int textLength;
        do {
            System.out.println("Введите длину сгенерированного текста (n <= 1000): ");
            textLength = scanner.nextInt();
        } while (textLength > 1000);
        scanner.nextLine(); // Переходим на новую строку после считывания числа

        // Считываем сгенерированный текст
        String generatedText = scanner.nextLine();

        // Вызываем метод для нахождения минимальной и максимальной длины строки
        int[] result = findMinMaxStringLength(generatedText);

        // Выводим результат в новом формате
        System.out.println(result[0] + " " + result[1]);
    }

    private static int[] findMinMaxStringLength(String text) {
        // Разделяем текст на строки
        String[] lines = text.split("#");

        // Инициализируем переменные для хранения минимальной и максимальной длины строки
        int minLength = Integer.MAX_VALUE;
        int maxLength = 0;

        // Проходим по каждой строке и обновляем значения minLength и maxLength
        for (String line : lines) {
            int length = line.length();
            minLength = Math.min(minLength, length);
            maxLength = Math.max(maxLength, length);
        }

        // Возвращаем результат в виде массива
        return new int[]{minLength, maxLength};
    }
}